vector<long> sieve(1,2);

template <typename I>
void make_sieve(I MAX) {
//create table of primes
  I M = MAX>>1;
  if (!(MAX & 1)) M--;
  X.resize(M+1,true);
  I k;
  for (I j=1,p=3,q=4;j<=M;j++, p+=2, q+=2*p-2) {
    if (X[j]==0) continue;
    else k=q;
    while (k <= M) {
      X[k] = 0;
      k += p;
    }
  }
  for  (I i=1;i<(I)X.size();i++) if (X[i]) sieve.push_back(2*i+1);
}

int calc(const vector<int>& aa) {
  int S = 1;
  for (int i=0;i<(int)sieve.size();i++) {
    for (int j=0;j<aa[i];j++) S *= sieve[i];
  }
  return S;
}

bigint cnv_double(double d, int p) {
    stringstream aa;
    aa << d;
    string s = aa.str();
    int pos = s.find('.'); 
    if (d<1.0) {
      s = s.substr(pos+1);
    }
    else {
      s.erase(pos,1);
    }
    s.append(p-s.length(),'0');
    return bigint(s);
}

bigint div10(bigint b, int p) {
    stringstream aa;
    aa << b;
    string s = aa.str();
    return bigint(s.substr(0,s.length()-p));
}

